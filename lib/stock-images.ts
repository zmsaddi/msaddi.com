/**
 * Helper function to get image URL with optional transformations
 */
export function getStockImage(
  category: keyof typeof stockImages,
  name: string,
  options?: {
    width?: number;
    height?: number;
    quality?: number;
  }
) {
  const image = (stockImages[category] as any)[name];

  if (!image) {
    console.warn(`Stock image not found: ${category}.${name}`);
    return null;
  }

  let url = image.url;

  if (options) {
    const params = new URLSearchParams();
    if (options.width) params.set('w', options.width.toString());
    if (options.height) params.set('h', options.height.toString());
    if (options.quality) params.set('q', options.quality.toString());

    url = `${url.split('?')[0]}?${params.toString()}`;
  }

  return {
    ...image,
    url,
  };
}

